package org.example;
/**
 * Clase que representa una bebida de tipo CocaCola.
 * Hereda de la clase {@link Bebida} y define su sabor como "CocaCola".
 */
public class CocaCola extends Bebida {
    /**
     * Constructor por defecto de la clase {@code CocaCola}.
     * Asigna el sabor "CocaCola" al producto.
     */
    public CocaCola() {
        super();

        this.sabor = "CocaCola";
    }
}