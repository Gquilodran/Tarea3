package org.example;
/**
 * Clase que representa una bebida de tipo Sprite.
 * Hereda de la clase {@link Bebida} y define su sabor como "Sprite".
 */
public class Sprite extends Bebida {
    /**
     * Constructor por defecto de la clase {@code Sprite}.
     * Asigna el sabor "Sprite" al producto.
     */
    public Sprite(){
        super();
        this.sabor="Sprite";
    }
}