package org.example.Logica;

/**
 * Clase que representa un Dulce de tipo Sniker.
 * Hereda de la clase {@link Dulce} y define su sabor como "Snicker".
 */
public class Sniker extends Dulce {
    /**
     * Constructor por defecto de la clase {@code Sniker}.
     * Asigna el sabor "Snicker" al producto.
     */
    public Sniker(){
        super();
        this.sabor="Snicker";
    }


}