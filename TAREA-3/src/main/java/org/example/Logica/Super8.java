package org.example.Logica;
/**
 * Clase que representa un Dulce de tipo Super8.
 * Hereda de la clase {@link Dulce} y define su sabor como "Super8".
 */
public class Super8 extends Dulce {
    /**
     * Constructor por defecto de la clase {@code Super8}.
     * Asigna el sabor "Super8"
     */
    public Super8(){
        super();
        this.sabor = "Super8";
    }
}