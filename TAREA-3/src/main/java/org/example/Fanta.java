package org.example;
/**
 * Clase que representa una bebida de tipo Fanta.
 * Hereda de la clase {@link Bebida} y define su sabor como "Fanta".
 */
public class Fanta extends Bebida {
    /**
     * Constructor por defecto de la clase {@code Fanta}.
     * Asigna el sabor "Fanta" al producto.
     */
    public Fanta() {
        super();
        this.sabor = "Fanta";
    }
}

